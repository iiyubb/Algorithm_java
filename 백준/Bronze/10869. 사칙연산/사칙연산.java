import java.util.Scanner;

public class Main {

	public static void main(String[] args) {
		Scanner input = new Scanner(System.in);
		int input1 = input.nextInt();
		int input2 = input.nextInt();
		
		System.out.println(input1 + input2);
		System.out.println(input1 - input2);
		System.out.println(input1 * input2);
		System.out.println(input1 / input2);
		System.out.println(input1 % input2);

	}

}
